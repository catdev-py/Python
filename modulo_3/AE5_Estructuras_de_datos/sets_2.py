
# MINI-TALLER: Sets en Python - Nivel básico
# Objetivo: Crear sets y realizar operaciones básicas

# Crear sets
mi_set = {1, 2, 3, 4}
otro_set = set([3, 4, 5, 6])
print("Set 1:", mi_set)
print("Set 2:", otro_set)

# Unión
print("Unión:", mi_set | otro_set)

# Intersección
print("Intersección:", mi_set & otro_set)

# Diferencia
print("Diferencia:", mi_set - otro_set)


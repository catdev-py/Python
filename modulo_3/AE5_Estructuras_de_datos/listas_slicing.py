# Lista de números para practicar, índices y Slicing

numeros = [10, 20, 30, 40, 50, 60, 70, 80]

# Mostrar desde la posición 2 hasta el final
print("Desde el tercer número hasta el final:", numeros[2:])

# Mostrar solo los primeros 4 números
print("Primeros cuatro números:", numeros[:4])

# Mostrar un rango intermedio (índices 3 a 5)
print("Del cuarto al sexto número:", numeros[3:6])

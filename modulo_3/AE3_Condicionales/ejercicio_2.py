""" 
Concepto Clave: ¿Por qué usamos Condicionales?
Los programas normalmente siguen instrucciones de forma lineal.

Las condicionales permiten que el flujo cambie, como cuando tú 
decides si ir al trabajo en auto o micro dependiendo del clima. 
"""

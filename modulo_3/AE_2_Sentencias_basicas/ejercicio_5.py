""" 
Ciclo while - Repite mientras algo sea verdadero
Cuando no sabemos cuántas veces vamos a repetir, usamos while. 
"""
contador = 1
while contador <= 5:
    print("Contando:", contador)
    contador += 1  # Suma 1 cada vez

# Importante: Debes modificar la condición dentro del bucle, ¡o se repite para siempre!
